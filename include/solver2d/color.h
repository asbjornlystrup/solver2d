static inline s2Color s2MakeColor(enum s2HexColor hexCode, float alpha)
{
	s2Color color;
	color.r = ((hexCode >> 16) & 0xFF) / 255.0f;
	color.g = ((hexCode >> 8) & 0xFF) / 255.0f;
	color.b = (hexCode & 0xFF) / 255.0f;
	color.a = alpha;
	return color;
}
